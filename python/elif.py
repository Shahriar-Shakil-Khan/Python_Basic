num=float(input("Enter number As you wish : "))

if num>=90:
    print("A+")

elif num>=85:
    print("A")
    
elif num>=80:
    print("B+")       

elif num>=75:
    print("B")

elif num>=70:
    print("C+")
    
elif num>=65:
    print("C")  
    
elif num>=60:
    print("D+")
    
elif num>=50:
    print("D")     
    
else:
    print("Fail")                   